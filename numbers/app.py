# Basic arithmetic

print(2 + 2)

print(2 - 2)

print(2 * 2)

print(2 / 2)

# Power operator
print(2 ** 5)
# Square root operator
print(4 ** 0.5)
# Order of operations
print(2+10 * 10+3)

print((2+10) * (10+3))

# Variable assignment
# It's a dynamic programming language
# You don't need to declare variable type before assignment
# Variable name cannot start with numbers or symbols
a = 5

print(a)

print(a+a)

# Python support reassignment
a = a*5

print(a)

# example of variable names
my_income = 100
tax_rate = 0.1
my_taxes = my_income * tax_rate
# Note the floating point number
print(my_taxes)
